                                    <h2 class="text-uppercase">Modelo OSI</h2>
                                    
                                    <img class="img-fluid d-block mx-auto" src="assets/img/portfolio/04-thumbnail.jpg" alt="..." />
                                    <p>El Modelo OSI
El modelo OSI (Modelo para la Interconexión de Sistemas Abiertos), Figura 1 se compone de siete niveles de proceso, mediante el cual los datos se empaquetan y se transmiten desde una aplicación emisora, viajando a través de medios físicos hasta llegar a una aplicación receptora.
<br>
<br>
<b>La capa de aplicación:</b> proporciona los servicios utilizados por las aplicaciones para que los usuarios se comuniquen a través de la red. Es el nivel más cercano al usuario.
<br>
<b>La capa de presentación:</b> define el formato de los datos que se van a intercambiar entre las aplicaciones, ofreciendo un conjunto de servicios para la transformación de datos.
<br>
<b>La capa de sesión:</b> proporciona los mecanismos para controlar el diálogo entre las aplicaciones de los sistemas finales: abre, mantiene y cierra la sesión entre dos sistemas.
<br>
La capa de transporte: permite intercambiar datos entre sistemas finales, dividiendo el mensaje en varios fragmentos. El servicio de transporte puede ser orientado o no orientado a conexión, tomando en cuenta la unidad de transferencia máxima (MTU).
<br>
<b>La capa de red:</b> se encarga de definir el camino que seguirán los datos desde el origen hasta su destino a través de una o más redes conectadas mediante dispositivos de enrutamiento (router).
<br>
<b>La capa de enlace de datos:</b> se ocupa del direccionamiento físico dentro de cualquier topología de red, esta capa nos permite activar, mantener y deshabilitar la conexión, así como la notificación de errores.
<br>
<b>La capa física:</b> controla las señales por donde viajaran los datos (cable de par trenzado, fibra óptica, radio frecuencia).


</p>
