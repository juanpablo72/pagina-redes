                                    <h2 class="text-uppercase">Topologia</h2>
                                    

Se denomina  topología de red a todo aquel modelo de interconexión según el cual estén  relacionadas  entre clientes y servidores. En pocas palabras es como un mapa físico o el mapa  lógico de una red en el cual se muestra el intercambio de datos. Es decir aquella  forma en que está diseñada la red, sea en un  plano físico o  un plano lógico. Una red se definiría como aquel conjunto de nodos interconectados entre si. Recordando que un nodo es el punto en el que una curva se intercepta a sí misma. Lo que un nodo es concretamente depende del tipo de red en cuestión.
<br>
<br>
Un claro ejemplo  de la topología de árbol, la cual se llama así, por su apariencia estética parecida a un árbol , puede comenzar con la inserción del servicio de internet desde el proveedor, pasando por el router, luego por un switch y este deriva a otro switch , otro router o sencillamente a los hosts “estaciones de trabajo”, el resultado de esto es una red con apariencia de árbol porque desde el primer router que se tiene se ramifica la distribución de Internet, dando lugar a la creación de nuevas redes o subredes tanto internas como externas. Además de la topología estética, se puede dar una topología lógica a la red y eso dependerá de lo que se necesite en el momento

