                                    <h2 class="text-uppercase">Tipos de redes</h2>
                                    
                                    <img class="img-fluid d-block mx-auto" src="assets/img/portfolio/02-full.jpg" alt="..." />
                                    <p>
En cuanto a los tipos de redes, se debe tener en cuenta que las redes informáticas se clasifican según su tamaño  y son las siguientes:
<br>
•  <b> Redes LAN</b> Siglas de Local Area Network (en inglés: “Red de Área Local”), se trata de un tipo de red de menor tamaño, y son comúnmente usadas en un consultorio o cyber café, o un departamento.
<br>
•   <b>Redes MAN </b>Siglas de Metropolitan Area Network (en inglés: “Red de Área Metropolitana”)  este tipo de red se designa a redes de tamaño intermedio, como las empleadas en los campus universitarios o en grandes bibliotecas o empresas, que conectan distintas áreas alejadas entre sí.
<br>
•   <b>Redes WAN </b>Siglas de Wide Area Network (en inglés: “Red de Área Amplia”), alude a las redes de mayor envergadura y alcance, como lo es la red global de redes, Internet.
